/**
 * Alchemy Engine — Passage Resolver (Graceful AI Fallback)
 *
 * Unified passage resolution service that detects AI availability
 * and serves the appropriate content source (AI-generated or static
 * template) transparently to consumers.
 *
 * Design principles:
 *   - Non-AI path is first-class: static content always available
 *   - Proactive detection: checks availability BEFORE attempting generation
 *   - Graceful degradation: never throws on AI failure, always returns content
 *   - Source transparency: every resolved passage reports its content source
 *   - Vertical slice: Challenger face + Wake Up WAVE move only
 *
 * Fallback chain (priority order):
 *   1. Forced static (when explicitly requested or AI disabled)
 *   2. AI generation (when available + not forced static)
 *   3. Cached AI response (when cache hit available)
 *   4. Template bank content (channel-typed, GM-authored)
 *   5. Static inline content (always available, never fails)
 *
 * Components consume `resolvePassage()` and `resolveReflectionCompletions()`
 * instead of directly calling AI or static builders. The resolver handles
 * all detection, fallback, and source attribution internally.
 *
 * @see ai-generation.ts — AI generation + static builders
 * @see template-bank-data.ts — GM-authored template bank seed data
 * @see reflection-generation.ts — reflection completion generation
 * @see template-bank-types.ts — ChannelContentSlot, resolveChannelContent
 */

