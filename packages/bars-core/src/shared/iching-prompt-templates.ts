/**
 * Get the face-angled question for a specific hexagram + face combination.
 * Returns the full entry if face is omitted.
 */
export function getIChingPromptTemplate(
  hexagramId: number,
  face?: GameMasterFace
): IChingPromptTemplateEntry | string {
  const entry = HEXAGRAM_PROMPT_TEMPLATES[hexagramId]
  if (!entry) {
    return `What does hexagram ${hexagramId} ask of you?`
  }
  if (!face) return entry
  return entry[face] ?? entry.base
}

/**
 * Get all face questions for a hexagram as an array (for face-picker UI).
 */
export function getFaceQuestionsForHexagram(hexagramId: number): Array<{ face: GameMasterFace; question: string }> {
  const entry = HEXAGRAM_PROMPT_TEMPLATES[hexagramId]
  if (!entry) return []
  return [
    { face: 'shaman',     question: entry.shaman },
    { face: 'challenger', question: entry.challenger },
    { face: 'regent',     question: entry.regent },
    { face: 'architect',  question: entry.architect },
    { face: 'diplomat',   question: entry.diplomat },
    { face: 'sage',       question: entry.sage },
  ]
}
