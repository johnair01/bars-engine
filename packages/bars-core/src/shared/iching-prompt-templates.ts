/**
 * I Ching Prompt Templates — Face-Anchored Questions
 * 
 * Every hexagram carries:
 *   - A BASE QUESTION (its wisdom theme as a prompt)
 *   - 6 FACE-ANGLED QUESTIONS — the base question filtered through each Game Master Face
 * 
 * HOW TO USE:
 *   import { getIChingPromptTemplate, HEXAGRAM_PROMPT_TEMPLATES } from '@/shared/iching-prompt-templates'
 *   
 *   // Get all 6 face questions for a hexagram
 *   const entry = HEXAGRAM_PROMPT_TEMPLATES[17]
 *   entry.shaman  // → "What in your body knows the answer to this before your mind does? What opportunity is..."
 * 
 *   // Or via the helper (returns structured object)
 *   const { base, faceQuestion, faceLabel } = getIChingPromptTemplate(17, 'challenger')
 * 
 * FACE ORDER (consistent with LINE_TO_FACE mapping in cast-iching.ts):
 *   shaman      → line 1 (bottom)  — body, roots, felt sense
 *   challenger  → line 2           — opposition, truth, edge
 *   regent      → line 3           — judgment, long arc, order
 *   architect   → line 4           — structure, strategy, blueprint
 *   diplomat    → line 5           — bridge, relationship, care
 *   sage        → line 6 (top)    — integration, wisdom, precedent
 * 
 * UX FLOW:
 *   1. Player draws hexagram
 *   2. Sees 6 face cards (not questions yet) — "Which face do you need to hear from?"
 *   3. Player picks a face — that choice IS the reading (they stake on which lens they need)
 *   4. Player sees that face's question for this hexagram
 *   5. Player writes their response → player_response BAR created with face + hexagram + response
 */

